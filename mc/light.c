static void init_without_timer(void)
{
	/* Initialisierung, falls Timer nicht verwendet wird. */
	set_dimm(7, 255);
	set_dimm(6, 224);
	set_dimm(5, 192);
	set_dimm(4, 160);
	set_dimm(3, 128);
	set_dimm(2,  96);
	set_dimm(1,  64);
	set_dimm(0,  32);
}

int main(void)
{
	/* Takt initialisieren, so dass MCP2515 seine 16 MHz auch her gibt. */
	/* Ansonsten passiert hier heute per CAN-Bus nix ;-) */
	mcp2515_init(0, 0, MCP2515_SINGLE_TX_BUFFER);
	port_init();
	timer1_init(F_CPU / (50 * PWM_TABLE_SIZE));
	timer2_init();

	do
	{
		/*
		 * Ein bißchen was für die Umwelt tun. Es fängt halt
		 * halt schon im Kleinen an ;-)
		 */
		sleep();
	}
	while(1);
}
